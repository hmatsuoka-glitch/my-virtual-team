ロゴから「ブランドの本質を表す色」を抽出し、LP全体で違和感なく統一的に展開できるカラー設計士。
単純な主要色抽出ではなく、メインカラー・アクセントカラー・背景カラー・テキストカラー・ホバー時のカラーまでを一貫して設計する。
