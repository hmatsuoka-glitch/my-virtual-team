#!/bin/bash
#
# migrate-lp-dept.sh
#
# 07-LP複製部 → 07-LP部 への組織改編をワンショットで実行する。
#
# 実施内容:
#   1. agents/07-LP複製部/ → agents/07-LP部/ にディレクトリリネーム（git mv）
#   2. 既存メンバー全員の "部署: 07-LP複製部" → "部署: 07-LP部" に置換
#   3. kaito.md の役職を「LP部部長 兼 複製係係長」に変更
#   4. 新規3エージェント追加:
#      - tsumugi（LP制作係 係長 / LP制作プロジェクトディレクター）
#      - iro（ロゴ→カラーコード抽出スペシャリスト）
#      - kotone（LPコピーライター）
#   5. SKILL.md の振り分けルール更新
#   6. README.md / AGENTS.md を sync-source-readmes.py で再生成
#   7. スキル版リポジトリへ commit & push
#   8. エージェント版リポジトリへ同期（sync-to-agents.sh）
#

set -uo pipefail

REPO_DIR="${HOME}/my-virtual-team"
LOG_FILE="${REPO_DIR}/logs/migrate-lp-dept.log"
DATE_STR=$(date +%Y-%m-%d)
mkdir -p "$(dirname "${LOG_FILE}")"

GREEN='\033[0;32m'
YELLOW='\033[1;33m'
RED='\033[0;31m'
BLUE='\033[0;34m'
NC='\033[0m'

info()    { echo -e "${BLUE}[INFO]${NC}    $1" | tee -a "${LOG_FILE}"; }
ok()      { echo -e "${GREEN}[OK]${NC}      $1" | tee -a "${LOG_FILE}"; }
warn()    { echo -e "${YELLOW}[WARN]${NC}    $1" | tee -a "${LOG_FILE}"; }
err()     { echo -e "${RED}[ERROR]${NC}   $1" | tee -a "${LOG_FILE}"; }

info "=== LP部改編 start ($(date +'%Y-%m-%d %H:%M:%S')) ==="

cd "${REPO_DIR}" || { err "cd 失敗"; exit 1; }

OLD_DIR="agents/07-LP複製部"
NEW_DIR="agents/07-LP部"

# 既に改編済みかチェック
if [ -d "${NEW_DIR}" ] && [ ! -d "${OLD_DIR}" ]; then
    warn "既にディレクトリは ${NEW_DIR} に改編済みです"
elif [ ! -d "${OLD_DIR}" ]; then
    err "${OLD_DIR} が見つかりません。中止します"
    exit 1
else
    # ---------------------------------------------------------------
    # STEP 1: ディレクトリリネーム
    # ---------------------------------------------------------------
    info "STEP 1: ${OLD_DIR} → ${NEW_DIR} にリネーム"
    git mv "${OLD_DIR}" "${NEW_DIR}" 2>>"${LOG_FILE}" || {
        err "git mv 失敗"
        exit 2
    }
    ok "ディレクトリリネーム完了"
fi

# ---------------------------------------------------------------
# STEP 2: 既存メンバーの部署表記を「07-LP部」に統一
# ---------------------------------------------------------------
info "STEP 2: 既存メンバーの部署表記を 07-LP部 に更新"

for f in "${NEW_DIR}"/*.md; do
    # H1 行（# Name — 07-LP複製部 / ...）の更新
    sed -i.bak -E 's|07-LP複製部|07-LP部|g' "$f"
    rm -f "$f.bak"
done

ok "部署表記の置換完了"

# ---------------------------------------------------------------
# STEP 3: kaito.md の役職を変更
# ---------------------------------------------------------------
info "STEP 3: kaito.md の役職を「LP部 部長 兼 複製係 係長 / LP複製プロジェクトディレクター」に変更"

KAITO="${NEW_DIR}/kaito.md"
if [ -f "${KAITO}" ]; then
    # 既に変更済みかチェック
    if grep -q "LP部 部長" "${KAITO}"; then
        warn "kaito.md は既に部長に変更済みです"
    else
        # H1 を変更: # Kaito — 07-LP部 / LP複製ディレクター → # Kaito — 07-LP部 / LP部部長 兼 複製係係長
        sed -i.bak -E 's|^# Kaito — 07-LP部 / .*|# Kaito — 07-LP部 / LP部部長 兼 複製係係長|' "${KAITO}"
        # 役職行を変更
        sed -i.bak -E 's|- \*\*役職\*\*: LP複製プロジェクトディレクター.*|- **役職**: LP部 部長 兼 複製係 係長 / LP複製プロジェクトディレクター|' "${KAITO}"
        rm -f "${KAITO}.bak"
        ok "kaito.md の役職を更新"
    fi
else
    err "${KAITO} が見つかりません"
    exit 3
fi

# ---------------------------------------------------------------
# STEP 4: 新規3エージェント追加
# ---------------------------------------------------------------
info "STEP 4: 新規エージェント tsumugi / iro / kotone を追加"

# tsumugi.md
TSUMUGI="${NEW_DIR}/tsumugi.md"
if [ -f "${TSUMUGI}" ]; then
    warn "tsumugi.md は既に存在します。スキップ"
else
    cat > "${TSUMUGI}" <<'EOF'
